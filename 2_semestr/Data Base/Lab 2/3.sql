-- Вывести число фамилий и отчеств без учета повторений.
-- При составлении запроса нельзя использовать DISTINCT.
SELECT count(*)
FROM (SELECT "ФАМИЛИЯ", "ОТЧЕСТВО"
    FROM "Н_ЛЮДИ"
    WHERE ("ОТЧЕСТВО", "ФАМИЛИЯ")
    IN
          (SELECT "ОТЧЕСТВО", "ФАМИЛИЯ"
                FROM "Н_ЛЮДИ" GROUP BY "ФАМИЛИЯ", "ОТЧЕСТВО"
                HAVING count(*)=1)
    ORDER BY "ФАМИЛИЯ")
AS rows_count;